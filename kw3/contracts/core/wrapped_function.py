# ---------------------------------------------------- class: WrappedFunction ---------------------------------------------------- #

class WrappedFunction:

    # --------------------------------------------------------- Init --------------------------------------------------------- #

    def __init__(
        self,
        real_function: callable
    ):
        self.__real_function = real_function


    # ---------------------------------------------------- Public methods ---------------------------------------------------- #

    def call(
        self,
        *args,
        **kwargs
    ) -> any:
        return self.__real_function(*args, **kwargs).call()


